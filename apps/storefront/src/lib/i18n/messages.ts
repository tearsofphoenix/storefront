export type I18nMessages = (typeof messages)["en-US"]

export function getMessages(locale: SupportedLocale = defaultLocale): I18nMessages {
  const baseMessages = messages["en-US"]
  const localeMessages = (messages[locale] ?? baseMessages) as Partial<I18nMessages>

  return Object.keys(baseMessages).reduce((result, scope) => {
    const scopedBase = baseMessages[scope as keyof I18nMessages]
    const scopedLocale = localeMessages[scope as keyof I18nMessages]

    return {
      ...result,
      [scope]: {
        ...scopedBase,
        ...(scopedLocale as Record<string, string> | undefined),
      },
    }
  }, {} as I18nMessages)
}
